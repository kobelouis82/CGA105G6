package com.goodsFig.model;

public class GoodsFigVO {
	private Integer figNo;
	private byte[] gamePic;
	private Integer serialNo;
	public Integer getFigNo() {
		return figNo;
	}
	public void setFigNo(Integer figNo) {
		this.figNo = figNo;
	}
	public byte[] getGamePic() {
		return gamePic;
	}
	public void setGamePic(byte[] gamePic) {
		this.gamePic = gamePic;
	}
	public Integer getSerialNo() {
		return serialNo;
	}
	public void setSerialNo(Integer serialNo) {
		this.serialNo = serialNo;
	}
}
